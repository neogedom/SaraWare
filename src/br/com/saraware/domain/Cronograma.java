package br.com.saraware.domain;

public class Cronograma {
	private Integer codigoCronograma;
	private RevisaoDeVidas revisaoDeVidas;
	
	public Integer getCodigoCronograma() {
		return codigoCronograma;
	}
	
	public void setCodigoCronograma(Integer codigoCronograma) {
		this.codigoCronograma = codigoCronograma;
	}
	
	public RevisaoDeVidas getRevisaoDeVidas() {
		return revisaoDeVidas;
	}
	
	public void setRevisaoDeVidas(RevisaoDeVidas revisaoDeVidas) {
		this.revisaoDeVidas = revisaoDeVidas;
	}
	

}
